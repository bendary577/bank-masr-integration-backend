package com.sun.supplierpoc.models.talabat;

public class CustomerMapping {

    private String name = "";
    private String foodicsId = "";
    private String talabatId = "";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFoodicsId() {
        return foodicsId;
    }

    public void setFoodicsId(String foodicsId) {
        this.foodicsId = foodicsId;
    }

    public String getTalabatId() {
        return talabatId;
    }

    public void setTalabatId(String talabatId) {
        this.talabatId = talabatId;
    }
}
